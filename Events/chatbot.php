            <h2 style="color:#A020F0">Chatbot</h2>
              CodeStrike organized a workshop in ChatBots and its Integrations on 31st March , 2017. This session was performed by using the API.AI website.API.AI is a platform for building conversational interfaces for bots, applications, and devices. This session was organized by our student committee members. The speakers of this session were Aaditya Tirodkar, Jeet Bafna,Burhanuddin Rampurawala and Rushikesh Patel. It was for a duration of 3 hours.

            <br><br>              

            Chat bots are computer programs that mimic conversation with people using artificial intelligence. They can transform the way you interact with the internet from a series of self-initiated tasks to a quasi-conversation. Chatbots can be integrated to your various services like Facebook, Slack Messenger, etc. Chatbots help in increasing your productivity by helping you in your day to day activities. 
            <h5>We wanted students to know the basics or the know hows of how to start the process of making a chat bot. Our timeline for this session was in the following order:-</h5>
            <div style="font-size: 125%">
               <ol>
                  <li>Introduction to api.ai</li>
                  <li>Basic Salutations</li>
                  <li>Making a bot for an e commerce clothing website</li>
                  <li>Mid break for sandwiches and water</li>
                  <li>Hands on session</li>
                  <li>Integration using Facebook messenger</li>
                  <li>Question and Answer</li>
                  <li>Feeback</li>
                </ol>
            </div>
            <br><br>                   
                We had a count of 40 students from all the three years. Student response was good during the session. As it was a interactive session, students were free to ask any doubts they had. Volunteers helped students in clearing their doubts and giving an idea about what is being done.
            <br><br>
                Prof Mahendra Patil, HOD of CMPN arrived at the end our session to get the feedback from the students . We got a very nice feedback from participants. Many students requested to have advanced version of the same. After the workshop Prof. Mahendra Patil and our committee had a talk about what else can be done in future to enhance the learning experience and placements of students of our college.
            <br><br>
                ChatBot workshop was a successful event due to all the efforts put in by the speakers and volunteers. We will keep organizing such events for students in future.
