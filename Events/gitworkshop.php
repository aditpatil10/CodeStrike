            <h2 style="color:#A020F0">Github Workshop</h2>
              The Department of Computer engineering , Atharva college of engineering , Malad(W) Mumbai, had organized a one day workshop and seminar for Github training on 17th July, 2017 at 1st Floor, Lab No.10 (CMPN) ,Phase 1 ACE. CodeStrike organized a workshop on Github training for faculty on 17th july, 2017. This session was performed by using the github.com website. Github is a development platform inspired by the way we work. From open source to business we can host and review code, manage projects and build software alongside millions of other developers. This session was organized by our student committee members. 
              <h6>The aim of the workshop was to provide training to the teachers on the use and benefits of Open Source Software and the following aspects of Github development platform was covered: 
                </h6>
             <div>
               <ol style="list-style-type:disc">
                  <li>Introduction to Github</li>
                  <li>Version Control</li>
                  <li>Gui Usage</li>
                  <li>Hand On Session</li>
                  <li>Command Line Usage</li>
                  <li>Question and Answer</li>
                </ol>
            </div>
            <br><br>              

                The workshop commenced at 2:00 P.M. and was split into 3 sessions.   Introduction to github, Version Control, Command line usage, GUI usage. Introduction to Github was covered in the first session from 2.00 P.M. to 3 P.M. Second session was between 3.15 P.M. and 4.00 P.M., Basic commands were explained with examples and exercises were completed. Each session followed by Question and Answer. Third session was between 4.00 P.M and 5.00 P.M. In the final session all the faculties tried to upload a project on github & share it to their peers. Besides that, they were also enlightened for how to efficiently use github to take a constant follow up of studentâ€™s projects. 
            <br><br>  
                We had a count of 24 teachers from CMPN dept. Faculty response was good during the session. As it was an interactive session, teachers were free to ask any doubts they had. Volunteers helped them in clearing their doubts and giving an idea about what is being done
.
           
            <br><br>                   
                After the completion of session, an online feedback was taken from the teachers using the Google forms.The feedback  taken considering various aspects right from the productivity of the session and how much it helped the faculties in assessing themselves. There was provision for giving remarks and suggestions as to how to improve the session further for the upcoming batches. Github training workshop was a successful event due to all the efforts put in by the speakers and volunteers. We will keep organizing such events for teachers in future. After the workshop Prof. Mahendra Patil and our committee had a talk about what else can be done in future to enhance the learning experience and placements of students of our college.

